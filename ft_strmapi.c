#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*dst;

	if (s == 0)
		return (0);
	i = 0;
	if (!(dst = (char*)malloc(ft_strlen((char*)s) + 1)))
		return (0);
	while (s[i] != '\0')
	{
		dst[i] = f(i, s[i]);
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
